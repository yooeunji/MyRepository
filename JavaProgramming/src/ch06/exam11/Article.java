package ch06.exam11;

public class Article {
	// Field
	int no;
	String title;
	String writer;
	int hit;
	String content;
	
	// Constructor
	Article() {}
	Article(int no, String title, String writer, int hit, String content) {
		this.no = no;
		this.title = title;
		this.writer = writer;
		this.hit = hit;
		this.content = content;
	}
	
	// Method

}
